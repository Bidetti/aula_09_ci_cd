def area_of_rectangle(width, height):
    area = width*height
    return area
 
def perimeter_of_rectangle(width, height):
    perimeter = 2 * (width + height)
    return perimeter

def soma(width, height):
    soma = width + height
    return soma

def subtracao(width, height):
    sub = width - height
    return sub

def multiplicacao(width, height):
    mult = width * height
    return mult

def divisao(width, height):
    div = width / height
    return div